# basic arithmetic

print(1 + 2)
# subtraction
print(5 - 2)
# floor_division
print(6 // 4)
'''
Floor division
using "floor" division. The // operator (two forward slashes) truncates the decimal without rounding, 
and returns an integer result.'''

# modulo
print(7 % 4)
'''4 goes into 7 once, with a remainder of 3. The % operator returns the remainder after division'''

# powers
print(2 ** 3)

# can also do root this way
print(4**0.5)

# Order of Operations followed in Python
print(2 + 3 - 1 * 3)

# Can use parentheses to specify orders
print((2+10) * (10+3))

